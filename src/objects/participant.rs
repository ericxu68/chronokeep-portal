use serde::{Serialize, Deserialize};

#[derive(Deserialize, Serialize, Debug, Clone)]
pub struct Participant {
    #[serde(skip)]
    id: u64,
    bib: String,
    first: String,
    last: String,
    birthdate: String,
    gender: String,
    age_group: String,
    distance: String,
    anonymous: bool,
}

impl Participant {
    pub fn new(
        id: u64,
        bib: String,
        first: String,
        last: String,
        birthdate: String,
        gender: String,
        age_group: String,
        distance: String,
        anonymous: bool,
    ) -> Participant {
        Participant {
            id,
            bib,
            first,
            last,
            birthdate,
            gender,
            age_group,
            distance,
            anonymous,
        }
    }

    pub fn equals(&self, other: &Participant) -> bool {
        self.bib == other.bib &&
        self.first == other.first &&
        self.last == other.last &&
        self.birthdate == other.birthdate &&
        self.gender == other.gender &&
        self.age_group == other.age_group &&
        self.distance == other.distance &&
        self.anonymous == other.anonymous
    }

    pub fn id(&self) -> u64 {
        self.id
    }

    pub fn bib(&self) -> &str {
        &self.bib
    }

    pub fn first(&self) -> &str {
        &self.first
    }

    pub fn last(&self) -> &str {
        &self.last
    }

    pub fn birthdate(&self) -> &str {
        &self.birthdate
    }

    pub fn gender(&self) -> &str {
        &self.gender
    }

    pub fn age_group(&self) -> &str {
        &self.age_group
    }

    pub fn distance(&self) -> &str {
        &self.distance
    }

    pub fn anonymous(&self) -> bool {
        self.anonymous
    }
}